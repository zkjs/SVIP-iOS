import Foundation

extension UIColor {
  
  class func ZKJS_mainColor() -> UIColor {
    return UIColor(hex: "#ffc56e")
  }
  
  class func ZKJS_textColor() -> UIColor {
    return UIColor(hex: "#666666")
  }
  
  class func ZKJS_navTitleColor() -> UIColor {
    return UIColor(hex: "#5e5e5e")
  }
  
  class func ZKJS_promptColor() -> UIColor {
    return UIColor(hex: "#b8b8b8")
  }
  
  class func ZKJS_lineColor() -> UIColor {
    return UIColor(hex: "#e3e3e3")
  }
  
  class func ZKJS_blackColor() -> UIColor {
    return UIColor(hex: "#101010")
  }
  
  class func ZKJS_whiteColor() -> UIColor {
    return UIColor(hex: "#f8f8f8")
  }
  class func ZKJS_navegationTextColor() -> UIColor {
  return UIColor(hex: "#949494")
  }
  
}